public class Repartidor {
    int posX;
    int posY;
    public Repartidor(int x,int y){
        this.posX=x;
        this.posY=y;
    }
    public int getPosX(){
        return this.posX;
    }
    public int getPosY(){
        return this.posY;
    }
}
